package com.rahim.domain;

public class RoomSelection {
	
	private int roomNo;
	
	
	private String american;
	private int americanAmt;
	
	private String french;
	private int frenchAmt;
	
	private String italian;
	private int italianAmt;
	
	private String pickUp;
	
	private String dropOff;
	
	private String parking;

	public int getRoomNo() {
		return roomNo;
	}

	public void setRoomNo(int roomNo) {
		this.roomNo = roomNo;
	}

	public String getAmerican() {
		return american;
	}

	public void setAmerican(String american) {
		this.american = american;
	}

	public int getAmericanAmt() {
		return americanAmt;
	}

	public void setAmericanAmt(int americanAmt) {
		this.americanAmt = americanAmt;
	}

	public String getFrench() {
		return french;
	}

	public void setFrench(String french) {
		this.french = french;
	}

	public int getFrenchAmt() {
		return frenchAmt;
	}

	public void setFrenchAmt(int frenchAmt) {
		this.frenchAmt = frenchAmt;
	}

	public String getItalian() {
		return italian;
	}

	public void setItalian(String italian) {
		this.italian = italian;
	}

	public int getItalianAmt() {
		return italianAmt;
	}

	public void setItalianAmt(int italianAmt) {
		this.italianAmt = italianAmt;
	}

	public String getPickUp() {
		return pickUp;
	}

	public void setPickUp(String pickUp) {
		this.pickUp = pickUp;
	}

	public String getDropOff() {
		return dropOff;
	}

	public void setDropOff(String dropOff) {
		this.dropOff = dropOff;
	}

	public String getParking() {
		return parking;
	}

	public void setParking(String parking) {
		this.parking = parking;
	}
	
	

}
